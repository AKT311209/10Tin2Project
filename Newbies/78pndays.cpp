#include <bits/stdc++.h>

using namespace std;


int main() {
    long long a;
    cin >> a;
    switch (a)
    {
        case 2:
            cout << "MONDAY";
            break;
        case 3:
            cout << "TUESDAY";
            break;
        case 4:
            cout << "WEDNESDAY";
            break;
        case 5:
            cout << "THURSDAY";
            break;
        case 6:
            cout << "FRIDAY";
            break;
        case 7:
            cout << "SATURDAY";
            break;
        case 8:
            cout << "SUNDAY";
            break;
    }
}
