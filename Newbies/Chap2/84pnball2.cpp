#include <bits/stdc++.h>

using namespace std;


int main() {
    long long n;
    cin >> n;
    switch (n%4)
    {
    case 1:
        cout << "ORANGE";
        break;
    case 2:
        cout << "ORANGE";
        break;
    case 0:
        cout << "GREEN";
        break;
    case 3:
        cout << "GREEN";
        break;
    
    default:
        break;
    }
}
